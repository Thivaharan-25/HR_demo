const calcByCode = (emp, code, opts = {}) => {
  if (code === "LK") return calcSL(emp);
  if (code === "GB") return calcUK(emp);
  if (code === "US") return calcUS(emp);
  if (code === "AU") return calcAU(emp);
  return calcOther(emp, opts.taxRate || 20);
};

const fmtNum = (n, currency) => {
  if (!n && n !== 0) return "—";
  if (currency === "GBP") return "£" + Math.abs(n).toLocaleString("en-GB");
  if (currency === "USD") return "$" + Math.abs(n).toLocaleString("en-US");
  if (currency === "AUD") return "A$" + Math.abs(n).toLocaleString("en-AU");
  return "LKR " + Math.abs(n).toLocaleString("en-US");
};

